let a = 5;
let b = "5";

// =  assignment (stores value)
let x = 10;
console.log(x);

// ==  compares value only
console.log(a == b);   // true

// === compares value + type
console.log(a === b);  // false

// !=  compares value only (not equal)
console.log(a != b);   // false

// !== compares value + type (not equal)
console.log(a !== b);  // true
