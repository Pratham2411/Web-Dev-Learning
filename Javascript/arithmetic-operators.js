// declaring variables
let a = 10;
let b = 3;

// addition
let sum = a + b;
console.log(`a + b = ${sum}`);

// subtraction
let diff = a - b;
console.log(`a - b = ${diff}`);

// multiplication
let mul = a * b;
console.log(`a * b = ${mul}`);

// division
let div = a / b;
console.log(`a / b = ${div}`);

// modulus (remainder)
let mod = a % b;
console.log(`a % b = ${mod}`);

// exponentiation (power)
let power = a ** b;
console.log(`a ** b = ${power}`);

// increment
let x = 5;
x++;
console.log(`After increment, x = ${x}`);

// decrement
let y = 8;
y--;
console.log(`After decrement, y = ${y}`);

// operator precedence
let result = 10 + 2 * 3;
console.log(`10 + 2 * 3 = ${result}`);

let result2 = (10 + 2) * 3;
console.log(`(10 + 2) * 3 = ${result2}`);
